//operator perbandingan hasil nya addalah bool

void main(){
  int a = 100;
  int b = 100;

  if (a == b){ //sama dengan
    print("a sama dengan b");
  } 
  if (a != b){ //tidak sama dengan
    print("a tidak sama dengan b");
  }
  if (a > b){ //lebih dari
    print("a lebih dari b");
  } 
  if (a < b){ //kurang dari
    print("a kurang dari b");
  }
  if (a >= b){//lebih dari atau sama dengan 
    print('a lebih dari atau sama dengan b');
  }
  if (a <= b){//kurang dari atau sama dengan
    print('a kurang dari atau sama dengan b');
  } 
}